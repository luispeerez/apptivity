            ContenidoMensaje.setDataHandler(new DataHandler(Adjunto));
            ContenidoMensaje.setFileName(Ruta);
            Multi.addBodyPart(ContenidoMensaje);
            Mensaje.setContent(Multi);
            Transport Enviar = Sesion.getTransport("smtp");
